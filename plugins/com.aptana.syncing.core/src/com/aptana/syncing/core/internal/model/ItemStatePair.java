package com.aptana.syncing.core.internal.model;

/**
 * 
 * @author Max Stepanov
 *
 */
/* package */ final class ItemStatePair {
	
	public final ItemState left;
	public final ItemState right;
	
	public ItemStatePair(ItemState left, ItemState right) {
		this.left = left;
		this.right = right;
	}
	
	protected ItemStatePair swap() {
		return new ItemStatePair(right, left);
	}
	
	protected boolean isNull() {
		return left == null && right == null;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((left == null) ? 0 : left.hashCode());
		result = prime * result + ((right == null) ? 0 : right.hashCode());
		return result;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (!(obj instanceof ItemStatePair)) {
			return false;
		}
		ItemStatePair other = (ItemStatePair) obj;
		if (left == null) {
			if (other.left != null) {
				return false;
			}
		} else if (!left.equals(other.left)) {
			return false;
		}
		if (right == null) {
			if (other.right != null) {
				return false;
			}
		} else if (!right.equals(other.right)) {
			return false;
		}
		return true;
	}

}